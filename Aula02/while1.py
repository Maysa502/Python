''' FOR X WHILE  
For:
Use quando você sabe exatamente quantas vezes quer fazer algo.
É como contar os dedos: você conta de 1 a 10, por exemplo.

While:
Use quando quer fazer algo enquanto uma condição for verdadeira.
É como continuar pulando enquanto a música está tocando.

Então, for é como uma contagem fixa, e while é como repetir algo até que algo aconteça ou deixe de ser verdadeiro. 
Ambos são úteis, mas escolha o que faz mais sentido para o que você quer fazer no seu código.'''
